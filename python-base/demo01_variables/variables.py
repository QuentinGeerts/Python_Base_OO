# Déclaration et affectation
x = 5
print(x)  # Affiche: 5s

# Déclaration de plusieurs variables en une seule ligne
a, b, c = 1, 2, 3
print(a, b, c)  # Affiche: 1 2 3



# Variable de type entier (int)
age = 30
print(age)  # Affiche: 30
print(type(age))

# Variable de type flottant (float)
taille = 1.75
print(taille)  # Affiche: 1.75
print(type(taille))

# Variable de type chaîne de caractères (str)
message = "Bonjour, tout le monde!"
print(message)  # Affiche: Bonjour, tout le monde!
print(type(message))

# Variable de type booléen (bool)
est_actif = True
print(est_actif)  # Affiche: True
print(type(est_actif))



# Noms de variables explicites et conformes aux conventions
nombre_de_livres = 5
prix_total = 59.99
utilisateur_connecte = True

print(nombre_de_livres)  # Affiche: 5
print(prix_total)        # Affiche: 59.99
print(utilisateur_connecte)  # Affiche: True
